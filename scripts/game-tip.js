document.addEventListener('DOMContentLoaded', () => {
    const tipButton = document.getElementById('tip-button');
    const tipText = document.getElementById('tip-text');
    if (!tipButton || !tipText) {
        console.warn('Элементы #tip-button или #tip-text не найдены');
        return;
    }

    const tips = [
        'На Прохоровке занимай кусты на холмах для разведки, но следи за флангами!',
        'Тяжёлые танки лучше танковать углом корпуса, чтобы увеличить шанс рикошета.',
        'В Химмельсдорфе используй здания как укрытие и контролируй центральные улицы.',
        'Лёгкие танки: свети врагов, но не вступай в бой без поддержки!',
        'ПТ-САУ эффективны в засадах — выбирай позиции с хорошим обзором и укрытием.',
        'На карте Рудники захватывай холм в центре для контроля карты.',
        'Следи за миникартой каждые 5–10 секунд, чтобы не пропустить манёвры врага.',
        'Используй рельеф местности: низины скрывают танк, а возвышенности дают обзор.',
        'Экономь снаряды: стреляй только по уязвимым зонам брони врага.',
        'В рандомных боях работай с командой — одиночки редко выживают!'
    ];

    tipButton.addEventListener('click', () => {
        const randomIndex = Math.floor(Math.random() * tips.length);
        tipText.textContent = tips[randomIndex];
    });
});